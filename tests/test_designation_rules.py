@pytest.mark.parametrize("input_name,expected", RULE_CASES)
def test_rule_match(input_name: str, expected: tuple[str, str] | None) -> None:
    result = match(input_name)
    if expected is None:
        assert result is None
    else:
        assert result is not None
        rule_name, formatted_name = expected
        assert result == (formatted_name, rule_name)


